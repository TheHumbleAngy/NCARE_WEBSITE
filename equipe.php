            Administration et Direction
        <p>La NCA Re est administrée par un conseil d'administration de huit (8) membres, tous des personnamités connues
           du monde de l'assurance et de la réassurance Africaine.</p>
        <p>Le <strong>PCA, M. Ibrahim KEITA</strong> est diplômé du cycle ingénieur de l'I.I.A de Yaoundé. Il exerce
           depuis 30 ans des fonctions de direction dans le secteur des assurances.
           Il a été DGA des AGF Côte d'Ivoire (ALLIANZ), Administrateur Provisoire d'AIA Côte d'Ivoire et de l'ARC
           Congo.</p>
        <p>La <strong>Direction Générale est assurée par M. Nazaire ABBEY</strong>. Il est titulaire d'une Maîtrise en
           Economie et d'un DESS en Assurance (I.I.A). Il a une grande expérience de l'Assurance et de la Réassurance
           accumulée successivement en Côte d'Ivoire a la NSIA, AXA, AGF (Allianz), SWISS Re puis AVENI Re.</p>
        <p>Le Directeur Général est entouré d'une équipe réduite mais professionnelle composée de cadres compétents,
           tous titulaires d'un diplôme supérieur d'assurances ou d'orientation juridique et financière.</p>
        <p>Cette équipe sera completée à court terme par des Responsables de Marchés Francophone, Anglophone et Arabe,
           expérimentés.</p>
