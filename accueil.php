        <p>
            La NCA Ré est une société privée de réassurance, créée à Abidjan en République de Côte d'Ivoire le 17
            novembre 2012.
        </p>
        <p>
            Elle est dotée d'un capital de 7.000.000.000 FCFA, soit 10.671.431 Euros repartis comme suit:
        <ul class="icon">
            <li>Sociétés d'Assurances et de Courtage en Assurances: 58,43 %</li>
            <li>Fonds d'evestissement privé: 11,43 %</li>
            <li>Sociétés industrielles: 15,72 %</li>
            <li>Personnes physiques: 14,42 %</li>
        </ul>
        <p>
            Les actionnaires sont tous des sociétés ou opérateurs économiques de la Côte d'Ivoire, du Bénin, du Burkina
            faso, du Togo, du Ghana et du Nigéria. L'objectif est d'atteindre dans un bref délai 10.000.000.000 FCFA
            (15.244.902 Euros) de ce capital.
        </p>
